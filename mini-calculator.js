a = 20
b = 5

// PENJUMLAHAN
const tambah = a + b
console.log("Hasil Penjumlahan 20 + 5 =", tambah);
// Output yang akan keluar adalah 25

// PENGURANGAN
const kurang = a - b
console.log("Hasil Pengurangan 20 - 5 =", kurang)
// Output yang akan keluar adalah 15

// PERKALIAN
const perkalian = a * b
console.log("Hasil Perkalian 20 x 5 =", perkalian)
// Output yang akan keluar adalah 100

// PEMBAGIAN
const pembagian = a / b 
console.log("Hasil Pembagian 20 : 5 =", pembagian)
// Output yang akan keluar adalah 4

// PERSENAN
const persen = a % b
console.log("Hasil Persenan 20%/5% =", persen)
// Output yang akan keluar adalah 0